#include "3-calc.h"

/**
 * op_add - computes the sum of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - computes the difference of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - computes the product of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - computes the ...
 * @a: first number
 * @b: second number
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - computes the ...
 * @a: first number
 * @b: second number
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
